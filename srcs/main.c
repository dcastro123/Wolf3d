static	void	ft_error(void)
{
	ft_putstr("Error, too many arguments");
	exit(0);
}

int				close_window_hook(t_env *e)
{
	mlx_destroy_window(e->mlx, e->win);
	exit(0);
}

void			print_controls(void)
{
	ft_putendl("Controls: \n\
			Move Forward: UP arrow\n\n\
	Rotate Left: LEFT arrow     Rotate Right: RIGHT arrow\n\n\
			Move Backward: DOWN arrow\n");
}

int				main(int ac, char **av)
{
	t_env *e;

	av = NULL;
	if (ac > 1)
		ft_error();
	if (!(e = (t_env*)malloc(sizeof(t_env))))
		return (0);
	init_window(e);
	set_hooks(e);
	print_controls();
	raycast(e);
	mlx_loop_hook(e->mlx, calc_movement, e);
	mlx_loop(e->mlx);
	return (0);
}
